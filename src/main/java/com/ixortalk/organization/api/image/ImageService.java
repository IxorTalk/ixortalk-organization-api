package com.ixortalk.organization.api.image;

import com.ixortalk.organization.api.config.MultipartConfiguration;
import feign.Response;
import org.springframework.cloud.openfeign.FeignClient;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestPart;
import org.springframework.web.multipart.MultipartFile;

import static org.springframework.http.MediaType.MULTIPART_FORM_DATA_VALUE;

@FeignClient(name = "image-service", url = "${ixortalk.server.image-service.url}", configuration = MultipartConfiguration.class)
public interface ImageService {

    @PostMapping(path = "/upload", consumes = MULTIPART_FORM_DATA_VALUE)
    Response uploadImage(@RequestPart("file") MultipartFile file, @RequestPart("key") String key);

}